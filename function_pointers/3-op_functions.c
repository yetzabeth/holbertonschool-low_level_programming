#include "3-calc.h"
/**
 * op_add - returns the sum
 * @a: argument
 * @b: argument
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns the difference
 * @a: argument
 * @b: argument
 * Return: a + b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns the product
 * @a: argument
 * @b: argument
 * Return: a + b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns the div
 * @a: argument
 * @b: argument
 * Return: a + b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the mod
 * @a: argument
 * @b: argument
 * Return: a + b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
